
let val = true
let v2: Int = 3

// if - else if - else
if val == true {    
    print("if statement")
} else if val != true {
    print("else if statement")
} else {
    print("else statement")
}


// switch - case - default
switch v2 {
case 1:
    print("case 1")

case 2:
    print("case 2")

case 3:
    print("case 3")
    
default:
    print("default case")
}


// ternerary
print(val ? "true" : "false")
