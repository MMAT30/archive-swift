// let - constant
// var - mutable

let v1: Int = 1234567           // Int
var v2: Float = 123.4567        // Float
var v3: Double = 123.456789     // Double
var v4: Bool = true             // Bool
var v5: Character = "z"         // Character
var v6: String = "string"       // String
var v7 = nil                    // nil

// min & max
print("Int Min & Max: ", Int.min, Int.max)

// typealias - creates custom type
typealias Number = Int
print(Number.max)

// type - returns type of variable
print(type(of:))

print(v1, v2, v3, v4, v5, v6, v7)


 